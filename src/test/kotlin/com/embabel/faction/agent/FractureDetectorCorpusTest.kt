/**
 * Corpus tests: real windowed score series from validated historical runs.
 *
 * ## Purpose
 * Each test asserts the historically correct [TensionPattern] for a known open-source event.
 * Ground truth comes from documented history (blog posts, announcements, GitHub records),
 * NOT from what the algorithm currently produces. If a test fails, tune [DetectorWeights]
 * until all corpus cases pass simultaneously — do NOT change the expected outcome to match
 * the algorithm.
 *
 * ## How to add a new case
 * 1. Decide the historically correct pattern FIRST (before running the tool).
 * 2. Run the analysis and paste the windowed score table here as a fixture.
 * 3. Write the test asserting the ground-truth pattern.
 * 4. If it fails, adjust weights in [DetectorWeights] defaults until all cases pass.
 *
 * ## Current corpus and ground truth
 * | Repo | Window | Expected | Ground truth source |
 * |------|--------|----------|---------------------|
 * | nodejs/node | 2013-06-01 → 2015-03-01 | FRACTURE_ADVERSARIAL_FORK | io.js fork Jan 2015 (factionSignal=null → backward compat) |
 * | rust-lang/rust | 2020-01-01 → 2020-08-01 | FRACTURE_IMMINENT | Mozilla layoffs Aug 2020 (pre-event) |
 * | rust-lang/rust | 2019-07-05 → 2021-06-01 | EXODUS | Mozilla layoffs — external forced departure, 41% mass drop, 2.0% core impact |
