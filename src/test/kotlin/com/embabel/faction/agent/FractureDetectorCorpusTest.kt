    /**
     * Dense window — active project with many reviewers (nodejs, rust scale).
     * edgeCount=50 comfortably exceeds minResolutionWindowEdges so genuine dips are counted.
     */
    private fun w(start: String, end: String, asymmetry: Double, modularity: Double) = WindowedScore(
        windowStart = Instant.parse("${start}T00:00:00Z"),
        windowEnd = Instant.parse("${end}T00:00:00Z"),
        asymmetryRatio = asymmetry,
        connectedComponents = 1,
        modularity = modularity,
        edgeCount = 50,
    )

    /**
     * Sparse window — low-activity project (terraform scale) or holiday period.
     * edgeCount=2 falls below minResolutionWindowEdges so asymmetry dips are ignored.
     */
    private fun sparse(start: String, end: String, asymmetry: Double, modularity: Double) = WindowedScore(
        windowStart = Instant.parse("${start}T00:00:00Z"),
        windowEnd = Instant.parse("${end}T00:00:00Z"),
        asymmetryRatio = asymmetry,
        connectedComponents = 1,
        modularity = modularity,
        edgeCount = 2,
    )

