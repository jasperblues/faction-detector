 * | hashicorp/terraform | 2021-06-01 → 2022-05-01 | GOVERNANCE_CRISIS | pre-BSL tension; rising at window end |
 * | babel/babel | 2018-01-01 → 2020-06-01 | ATTRITION | Babel 7 release crunch Aug–Oct 2018; gradual resolution Dec 2018 |
 * | redis/redis | 2021-01-01 → 2023-12-01 | FRACTURE_IMMINENT | pre-Valkey tension; rising at window end |
 * | redis/redis | 2021-01-01 → 2024-09-01 | FRACTURE_ADVERSARIAL_FORK | Valkey fork March 2024, 26% mass drop |
