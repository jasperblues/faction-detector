/**
 * Corpus tests: real windowed score series from validated historical runs.
 *
 * ## Purpose
 * Each test asserts the historically correct [TensionPattern] for a known open-source event.
 * Ground truth comes from documented history (blog posts, announcements, GitHub records),
 * NOT from what the algorithm currently produces. If a test fails, tune [DetectorWeights]
 * until all corpus cases pass simultaneously — do NOT change the expected outcome to match
 * the algorithm.
 *
 * ## How to add a new case
 * 1. Decide the historically correct pattern FIRST (before running the tool).
 * 2. Run the analysis and paste the windowed score table here as a fixture.
 * 3. Write the test asserting the ground-truth pattern.
 * 4. If it fails, adjust weights in [DetectorWeights] defaults until all cases pass.
 *
 * ## Current corpus and ground truth
 * | Repo | Window | Expected | Ground truth source |
 * |------|--------|----------|---------------------|
 * | nodejs/node | 2013-06-01 → 2015-03-01 | FRACTURE_OCCURRED | io.js fork Jan 2015 |
 * | rust-lang/rust | 2020-01-01 → 2020-08-01 | FRACTURE_IMMINENT | Mozilla layoffs Aug 2020 (pre-event) |
 * | rust-lang/rust | 2020-01-01 → 2021-06-01 | ATTRITION | Mozilla layoffs — external forced departure, no fork |
 * | hashicorp/terraform | 2022-06-01 → 2024-06-01 | FRACTURE_OCCURRED | BSL licence change Aug 2023, OpenTofu fork Sep 2023 |
 * | hashicorp/terraform | 2021-06-01 → 2022-05-01 | FRACTURE_IMMINENT | pre-BSL tension; rising at window end |
 * | babel/babel | 2018-01-01 → 2020-06-01 | EXODUS | Babel 7 release crunch Aug–Oct 2018; gradual resolution Dec 2018 |
 * | redis/redis | 2021-01-01 → 2023-12-01 | FRACTURE_IMMINENT | pre-Valkey tension; rising at window end |
 * | redis/redis | 2021-01-01 → 2024-09-01 | FRACTURE_OCCURRED | Valkey fork March 2024, 38% mass drop |
 *
 * ## Known weight sensitivities
 * - rust ATTRITION: departureCentralityFraction=11.7% vs attritionCoreThreshold=12% — razor thin.
 *   Lowering attritionCoreThreshold below 11.7% flips this to EXODUS.
 * - terraform: sparse contributor pool produces 0.00/low asymmetry windows during quiet periods
 *   (holidays, summer lulls) — NOT genuine resolutions. Fixed by gating hadBriefResolution on
 *   edgeCount >= minResolutionWindowEdges. Terraform fixtures mark known sparse windows with
 *   sparse() helper (edgeCount=2); real runs will populate edgeCount from actual edge data.
 */
